r3=Radiobutton(win,text="3",value="3",variable=s)
r3.grid(row=3,column=2)
r4=Radiobutton(win,text="4",value="4",variable=s)
r4.grid(row=3,column=3)
r5=Radiobutton(win,text="5",value="5",variable=s)
